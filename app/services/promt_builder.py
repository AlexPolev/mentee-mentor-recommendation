import json
from typing import Any


def build_yandex_gpt_recommendation_prompt(
    mentee: dict[str, Any],
    mentors: list[dict[str, Any]],
    top_n: int,
) -> str:
    return f"""
Ты — интеллектуальная рекомендательная система для подбора менторов менти.

У тебя есть:
1. Один конкретный профиль менти.
2. Список профилей менторов.

Твоя задача:
1. Самостоятельно определить, по каким критериям лучше рекомендовать менторов этому менти.
2. Проанализировать профиль менти.
3. Сравнить менти со всеми менторами.
4. Выбрать top-{top_n} наиболее подходящих менторов.
5. Объяснить, почему каждый рекомендованный ментор подходит.
6. Указать возможные риски или ограничения по каждому ментору.
7. Кратко описать общую логику рекомендации.

Важно:
- Не используй заранее заданную формулу.
- Не придумывай факты, которых нет в JSON.
- Если данных недостаточно, прямо напиши об этом в possible_risks.
- Если ментор не подходит, не включай его в рекомендации, если хватает более подходящих кандидатов.
- Рекомендации должны быть отсортированы от наиболее подходящего ментора к менее подходящему.
- Ответ должен быть только в формате JSON.
- Не добавляй markdown.
- Не добавляй текст вне JSON.

Формат ответа строго такой:

{{
  "mentee_id": "{mentee.get("id")}",
  "recommendation_logic": "Опиши, как ты решил подбирать менторов для этого менти",
  "recommendations": [
    {{
      "mentor_id": "id ментора",
      "mentor_name": "имя ментора",
      "rank": 1,
      "score": 95,
      "explanation": "Подробное объяснение, почему этот ментор подходит этому менти",
      "matched_factors": [
        "Совпала цель менти с экспертизой ментора",
        "Совпал формат работы"
      ],
      "possible_risks": [
        "Есть риск, если данных недостаточно"
      ]
    }}
  ],
  "general_explanation": "Общий вывод по подбору"
}}

Профиль менти:
{json.dumps(mentee, ensure_ascii=False, indent=2)}

Список менторов:
{json.dumps(mentors, ensure_ascii=False, indent=2)}
""".strip()